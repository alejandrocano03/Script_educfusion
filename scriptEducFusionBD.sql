-- Crear la base de datos
CREATE DATABASE IF NOT EXISTS educFusionBD;
USE educFusionBD;

-- Crear las tablas
CREATE TABLE USUARIO (
    ID_Usuario INT PRIMARY KEY,
    Nombre VARCHAR(50),
    Apellido VARCHAR(50),
    Correo_Electronico VARCHAR(100),
    Contraseña_Cifrada VARCHAR(255),
    Rol VARCHAR(20),
    Fecha_Registro DATE,
	Ultima_Fecha_Acceso DATE
);

CREATE TABLE CATEGORIA_CENTROS_EDUCATIVOS (
    ID_Categoria INT PRIMARY KEY,
    Nombre VARCHAR(50),
    Descripcion_Breve TEXT,
    Subcategorias TEXT
);

CREATE TABLE CENTRO_EDUCATIVO (
    ID_Centro INT PRIMARY KEY,
    Nombre VARCHAR(100),
    Direccion VARCHAR(150),
    Tipo_Centro VARCHAR(50),
    Numero_Alumnos INT,
    Numero_Maestros INT,
    Niveles_Educativos_Ofrecidos VARCHAR(100),
    Coordenadas_Geograficas VARCHAR(50),
    ID_Categoria INT,
    Datos_Rendimiento_Academico TEXT,
    FOREIGN KEY (ID_Categoria) REFERENCES CATEGORIA_CENTROS_EDUCATIVOS(ID_Categoria)
);

CREATE TABLE ESTADISTICA_RENDIMIENTO_EDUCATIVO (
    ID_Estadistica INT PRIMARY KEY,
    ID_Centro INT,
    Año_Academico INT,
    Nivel_Educativo VARCHAR(50),
    Numero_Alumnos INT,
    Promedio_Calificaciones DECIMAL(3, 2),
    Numero_Alumnos_NEEs INT,
    Datos_Especificos_Rendimiento TEXT,
    FOREIGN KEY (ID_Centro) REFERENCES CENTRO_EDUCATIVO(ID_Centro)
);

CREATE TABLE OPINION_DE_USUARIO (
    ID_Opinion INT AUTO_INCREMENT PRIMARY KEY,
    ID_Usuario INT,
    ID_Centro INT,
    Fecha_Opinion DATE,
    Comentario TEXT,
    Valoracion INT,
    Ubicacion_Comentario VARCHAR(150),
    FOREIGN KEY (ID_Usuario) REFERENCES USUARIO(ID_Usuario),
    FOREIGN KEY (ID_Centro) REFERENCES CENTRO_EDUCATIVO(ID_Centro)
);

CREATE TABLE INFORME (
    ID_Informe INT AUTO_INCREMENT PRIMARY KEY,
    ID_Centro INT,
    Tipo_Informe VARCHAR(50),
    Datos_Incluidos TEXT,
    Fecha_Generacion DATE,
    Autor_Informe INT,
    FOREIGN KEY (ID_Centro) REFERENCES CENTRO_EDUCATIVO(ID_Centro),
    FOREIGN KEY (Autor_Informe) REFERENCES USUARIO(ID_Usuario)
);

CREATE TABLE MAPA_INTERACTIVO (
    ID_Mapa INT AUTO_INCREMENT PRIMARY KEY,
    ID_Centro INT,
    Coordenadas_Geograficas VARCHAR(50),
    Parametros_Zoom VARCHAR(50),
    Parametros_Centro TEXT,
    Capas_Detalles TEXT,
    FOREIGN KEY (ID_Centro) REFERENCES CENTRO_EDUCATIVO(ID_Centro)
);

CREATE TABLE RESULTADO_ACADEMICO (
    ID_Resultado INT PRIMARY KEY,
    ID_Centro INT,
    Año_Academico INT,
    Nivel_Educativo VARCHAR(50),
    Datos_Especificos_Rendimiento TEXT,
    FOREIGN KEY (ID_Centro) REFERENCES CENTRO_EDUCATIVO(ID_Centro)
);

